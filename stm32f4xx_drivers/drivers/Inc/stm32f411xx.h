#ifndef INC_STM32F411XX_H_
#define INC_STM32F411XX_H_


#define FLASB_BASEADDR			0x08000000U
#define SRAM1_BASEADDR			0x20000000U		//112KB
#define SRAM2_BASEADDR			0x20001C00U
#define ROM_BASEADDR			0x1FFF0000U
#define SRAM SRAM1_BASEADDR		SRAM1_BASEADDR

#define PERIPH_BASEADDR				0x4000000U
#define APB1PERIPH_BASEADDR			PERIPH_BASEADDR
#define APB2PERIPH_BASEADDR			0x40010000U
#define AHB1PERIPH_BASEADDR			0x40020000U
#define AHB1PERIPH_BASEADDR			0x50000000U

// main RAM is actually the SRAM1
// So, the SRAM1 is called as main SRAM, that we simply call as SRAM

// Mentioning "U" -> indicating the compiler that this value is not signed, but unsigned

#define GPIOA_BASEADDR			(AHB1PERIPH_BASEADDR + 0x0000)
#define GPIOB_BASEADDR			(AHB1PERIPH_BASEADDR + 0x0400)
#define GPIOC_BASEADDR			(AHB1PERIPH_BASEADDR + 0x0800)
#define GPIOD_BASEADDR			(AHB1PERIPH_BASEADDR + 0x0C00)
#define GPIOE_BASEADDR			(AHB1PERIPH_BASEADDR + 0x1000)
#define GPIOF_BASEADDR			(AHB1PERIPH_BASEADDR + 0x1400)
#define GPIOG_BASEADDR			(AHB1PERIPH_BASEADDR + 0x1800)
#define GPIOH_BASEADDR			(AHB1PERIPH_BASEADDR + 0x1C00)
#define GPIOI_BASEADDR			(AHB1PERIPH_BASEADDR + 0x2000)


#define I2C1_BASEADDR			(AHB1PERIPH_BASEADDR + 0x5400)
#define I2C2_BASEADDR			(AHB1PERIPH_BASEADDR + 0x5800)
#define I2C3_BASEADDR			(AHB1PERIPH_BASEADDR + 0x5C00)

#define SPI2_BASEADDR			(AHB1PERIPH_BASEADDR + 0x3800)
#define SPI2_BASEADDR			(AHB1PERIPH_BASEADDR + 0x3C00)

#define USART2_BASEADDR			(AHB1PERIPH_BASEADDR + 0x4400)
#define USART3_BASEADDR			(AHB1PERIPH_BASEADDR + 0x4800)
#define UART4_BASEADDR			(AHB1PERIPH_BASEADDR + 0x4C00)
#define UART5_BASEADDR			(AHB1PERIPH_BASEADDR + 0x5000)

#define EXTI_BASEADDR 			(AHB2PERIPH_BASEADDR + 0x3C00)
#define SPI1_BASEADDR			(AHB2PERIPH_BASEADDR + 0x3000)
#define SYSCFG_BASEADDR			(AHB2PERIPH_BASEADDR + 0x3800)
#define USART1_BASEADDR		(AHB2PERIPH_BASEADDR + 0x1000)
#define USART6_BASEADDR			(AHB2PERIPH_BASEADDR + 0x1400)

#endif /* INC_STM32F411XX_H_ */
